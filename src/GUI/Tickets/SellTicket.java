    private void kaydetActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_kaydetActionPerformed
        if(edit){
            db.clients.updateClient(user.id, isim.getText(),
                soyisim.getText(),
                tc.getText(),
                hes.getText(), 
                cinsiyet.getSelectedItem().toString());
            db.tickets.UpTicket(
                    db.tickets.getTicketID(user.id, sefer_ID),
                    sefer_ID, Double.valueOf(fiyat.getText()),
                    Integer.valueOf(koltuk.getText().split(":")[1].replace(" ", "")), 
                    user.id);
        }else{
            db.clients.addClient(
                isim.getText(),
                soyisim.getText(),
                tc.getText(),
                hes.getText(), 
                DBClients.Gender.valueOf(cinsiyet.getSelectedItem().toString())
            );
            db.tickets.addTicket(
                    this.sefer_ID,
                    Double.valueOf(fiyat.getText()),
                    Integer.valueOf(koltuk.getText().split(":")[1].replace(" ", "")),
                    db.clients.getLastClientID()
            );
        }
        if (form != null) {
             form.updateTable();
        }
        this.dispatchEvent(new WindowEvent(this, WindowEvent.WINDOW_CLOSING));
    }//GEN-LAST:event_kaydetActionPerformed

