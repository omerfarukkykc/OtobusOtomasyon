    public SellTicket(int sefer_ID,int userid,ListTickets form) {
        
        initComponents();
        this.form = form;
        this.sefer_ID = sefer_ID;
        Dimension dim = Toolkit.getDefaultToolkit().getScreenSize();
        this.setLocation(dim.width/2-this.getSize().width/2, dim.height/2-this.getSize().height/2);
        DefaultListModel lm2 = new DefaultListModel();
        for (int i = 1; i<=db.buses.getBus(1).koltukSayisi;i++){
            if(!db.tickets.getChairNumbers(sefer_ID).contains(i)){
                lm2.addElement("Koltuk : "+i);
            }
        }
        jList1.setModel(lm2);
        Client user = db.clients.getClient(userid);
        isim.setText(user.firstname);
        soyisim.setText(user.lastname);
        tc.setText(user.tc);
        hes.setText(user.hescode);
        cinsiyet.setSelectedIndex((user.gender == Gender.ERKEK)? 0 :1 );
        this.user = user;
        koltuk.setText("Koltuk : "+db.tickets.getChairNumber(userid, sefer_ID));
        kaydet.setText("Güncelle");
        edit = true;
        fiyat.setText((String.valueOf(db.tickets.getPrice(userid, sefer_ID))));
        
        
    }

